package java8Features.MethodReference;

public class Data {
	
	//static method

	public static void execute1() {
		System.out.println("Hello User");
	}
	
	//instance method
	
	public void execute2() {
		System.out.println("Welcome User");
	}
	
	//constr
	public Data() {
		System.out.println("Bye");
	}
	
}